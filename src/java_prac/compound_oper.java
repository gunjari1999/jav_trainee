package java_prac;

public class compound_oper {
    public static void main(String[] args) {
        int a = 25;
        a += 25;
        int b = 25;
        b *= 15;
        int c = 25;
        c -= 5;
        int d = 25;
        d /= 20;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}
